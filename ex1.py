# 1 - Ler 2 números inteiros, executar e mostrar o resultado das seguintes operações: adição, subtração, multiplicação e divisão.

#declaração de variáveis
n1 = int (input ('Digite o 1º número: '))
n2 = int (input ('Digite o 2º número: '))

#cálculos
print (f"O resultado da soma dos números foi: {n1+n2}")
print (f"O resultado da substração dos números foi: {n1-n2}")
print (f"O resultado da multiplicação dos números foi: {n1*n2}")
print (f"O resultado da divisão dos números foi: {n1/n2}")